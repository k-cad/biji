# 练习:
#   写一个程序,打印一个高度为4行的矩形方框
#      要求输入一个整数,此整数代码矩形的宽度,打印
#      相应宽度的矩形
#     如:
#       请输入矩形宽度: 10
#     打印如下:
#       ##########
#       #        #
#       #        #
#       ##########

w = int(input("请输入矩形的宽度: "))

print('#' * w)  # 打印第一行
print('#', ' ' * (w-2), '#', sep='')  # 第二行
print('#', ' ' * (w-2), '#', sep='')
print('#' * w)


