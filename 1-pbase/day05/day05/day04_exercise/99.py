#   5. 打印九九乘法表(需用到循环嵌套)
#     1x1=1
#     1x2=2 2x2=4
#     1x3=3 2x3=6 3x3=9
#     ....
#     1x9=9 ................... 9x9=81

# 第一重循环控制行数
y = 1
while y <= 9:
    # 此处打印一行的内容,每行打印y个乘法
    x = 1
    while x <= y:
        # 打印x*y的一列
        print('%dx%d=%-2d' % (x, y, x*y), end=' ')
        x += 1
    print()  # 换行
    y += 1