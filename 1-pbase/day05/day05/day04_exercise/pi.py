#   4. 写程序求:
#     1/1 - 1/3 + 1/5 - 1/7 + .......+ (+-)1/(2*n-1)的和
#     1) 求当n取1000000时这个算式的和
#     2) 求当n取1000000时这个算式的和乘4是多少
#        (答案: 3.1415....)

s = 0  # 累加和

n = 1  # 分母
sign = 1  # 符号
while n <= 1000000:
    s += sign * 1 / (2 * n - 1)
    sign *= -1  # 改变符号的正负
    n += 1
print("和是:", s)
print("4位的和是:", s * 4)  # 3.1415
