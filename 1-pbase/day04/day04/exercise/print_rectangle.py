# 练习:
#   输入一个整数n,打印一个宽度和高度都为n个字符的长方形
#   如:
#     请输入: 4
#   打印:
#     ####
#     #  #
#     #  #
#     ####
#   如:
#     请输入: 6
#   打印:
#     ######
#     #    #
#     #    #
#     #    #
#     #    #
#     ######

w = int(input("请输入矩形宽度: "))
# 1. 打印第一行
print('#' * w)

# 2. 打印中间的 w-2行
i = 1
while i <= w-2:  # 此循环会走 w-2次
    print("#" + ' ' * (w-2) + '#')
    i += 1

# 3. 打印最后的一行
if w >= 2:
    print('#' * w)


