# 练习:
#   1. 输入一个整数，代表树干的高度,打印如下一棵＂圣诞树"
#     如:
#       请输入: 2
#     打印
#      *
#     ***
#      *
#      *
#     如:
#       请输入: 3
#     打印:
#       *
#      ***
#     *****
#       *
#       *
#       *

h = int(input("请输入树干的高度: "))

# 打印树冠 部分
w = 2 * h - 1  # w 代表树冠最大下层叶子的星号个数
for line in range(1, h + 1): # line 代表行号
    stars = 2 * line - 1  # 算出星号个数
    s = '*' * stars
    print(s.center(w))

# 打印树干部分
for _ in range(h):
    print(' ' * (h-1) + '*')





