# 练习:
#   已知有列表:
#     L = [3, 5]
#   1) 用索引和切片操作，将原列表改变为:
#     L = [1, 2, 3, 4, 5, 6]
#   2) 将列表反转，删除最后一个元素后，打印此列表:
#     ...
#     print(L)  # [6, 5, 4, 3, 2]

L = [3, 5]

print("id(L)", id(L))  # ??????
L[1:1] = [4]  # L = [3, 4, 5]
L[len(L):] = [6]
L[0:0] = [1, 2]
print('L=', L)

# 将列表反转，删除最后一个元素后，打印此列表:
# L = L[::-1]  # <<<<------ L绑定新的列表ID会变化
L[::] = L[::-1]  # ID不变
del L[-1]
print("L=", L)
print("id(L):", id(L))


